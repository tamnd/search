package msgpack

import "sort"

// sortedKeys returns the keys of m in ascending order so map encoding is
// deterministic: the same document always produces the same bytes, which keeps
// stored blocks reproducible and lets the compression tests compare sizes.
func sortedKeys(m map[string]any) []string {
	keys := make([]string, 0, len(m))
	for k := range m {
		keys = append(keys, k)
	}
	sort.Strings(keys)
	return keys
}

func sortedStringKeys(m map[string]string) []string {
	keys := make([]string, 0, len(m))
	for k := range m {
		keys = append(keys, k)
	}
	sort.Strings(keys)
	return keys
}
